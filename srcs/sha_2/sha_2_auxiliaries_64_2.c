#include "ft_ssl_md5.h"

uint64_t	sha_2_ch_64(uint64_t x, uint64_t y, uint64_t z)
{
	return ((x & y) ^ ((~x) & z));
}

uint64_t	sha_2_maj_64(uint64_t x, uint64_t y, uint64_t z)
{
	return ((x & y) ^ (x & z) ^ (y & z));
}
